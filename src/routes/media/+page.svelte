<h1>Media I'm enjoying</h1>
<p>
    Not so short, non-comprehensive list of stuff I've been playing/watching lately.
</p>

<h2>Games!</h2>
<hr>
<h3>Persona 5 (PS3)</h3>
<p>
    I started playing this one a couple of days ago (22 Aug 2025). Really
    liking it so far! I'm not typically into turn-based combat, but this one cuts
    the fat and adds a lot of feedback during play. It's really satisfying.
</p>
<p>
    I'm only like, 7 hours in, so I can't really say much other than that I want
    to go back to playing it...
</p>

<h3>Eastward (PC)</h3>
<p>
    Bought it on GOG after eyeing it for a while. I've heard mixed reviews about the
    writing in the game, but I'm early on and I really like it. Time will tell.
    I do like weird things, and lots of dialogue doesn't bother me.
</p>
<p>
    Everything about the art direction is incredible. It's surprising how much
    lighting can add to immersion, so many of the scenes remind me of real life places.
    Again, not very far in yet, it's a long one.
</p>

<h3>WipEout HD / 2048 (PS3, PS4, PS Vita)</h3>
<p>
    These are games I've had for years but only recently been able to appreciate.
</p>
<p>
    As a kid, it was difficult to enjoy this series because I would
    just hit the walls over and over. But now that I'm older, I can finish a track
    without exploding my ship. So I've been playing it a lot more now.
</p>
<p>
    My favorite track is the Amphiseum.
</p>

<h3>Proteus (PC, PS Vita)</h3>
<p>
    It's a walking simulator. I've "finished" it, but I still turn it on sometimes when
    I feel that other games are just too much. As well as being very pretty, it's vaguely
    nostalgic for me- nostalgia for the style of indie pixel art games I played at a
    younger age. Like FEZ, or Minecraft.
</p>
<p>
    Proteus' world vexes me. I've seen each biome, the major pre-programmed events of the
    game, but I don't really understand what it's trying to say (if anything in particular).
    I don't get the backstory of its world. An island in the middle of an infinite ocean,
    with strange images of various creatures built atop the mountains, mysterious houses that
    eminate a warm home-y tune spread about, and stone circles that magically time-travel?
    What?
</p>
<p>
    But I sort of appreciate that I still have these questions when I decide to play. I like the
    mystery. It is a strange game.
</p>

<h3>The Witness (PC)</h3>
<p>
    Loving it so far. Based on my solved panel count, I'm about 1/5th of the way through this game.
    I like how it teaches you the rules to each puzzle type by letting you experiment with them.
    It's an experience of constant discovery.
</p>
<p>
    I savor things like this so I don't expect myself to finish it until maybe later next year
    or something.
</p>

<h2>TV!</h2>
<hr>
<h3>Bee and PuppyCat</h3>
<p>
    I love this show. Maybe too much. I've rewatched the Netflix series (Lazy in Space)
    at least 4 times ??? and I'll probably do that again soon cause I feel like I get
    so much out of each rewatch.
</p>
<p>
    I'm at that "reading fanfics for the first time kicking my legs up and down" stage
    of my illness. It's a really funny, cute and pretty show and I absolutely adore
    everything about it. The acting, the characters, the environments, the worldbuilding ...
    this show has permanently altered my brain chemistry.
</p>
<p>
    It's not for everyone and it's far from the conventional storytelling found in most shows-
    it's more like a slice-of-life where the setting leaves you with enough puzzle pieces to
    the big picture if you want to fully understand it, but otherwise is mostly about the
    character interactions.
</p>
<p>
    I get to watch it when I want to put something cozy and funny on, but also when I want to
    obsess over story details I missed, hidden in plain sight ...
</p>
<p>
    I'm biased, would recommend to weirdos.
</p>

